import Foundation
import UIKit

extension UIColor {
    class func mainColor() -> UIColor {
        return UIColor(red:0.22, green:0.22, blue:0.28, alpha:1.0)
    }
    
    class func warningColor() -> UIColor {
        return UIColor(red:0.90, green:0.35, blue:0.33, alpha:1.0)
    }
}